// Port F Initialization
void PortF_Init(void){
  SYSCTL_RCGC2_R |= SYSCTL_RCGC2_GPIOF; // (a) activate clock for port F
	while ((SYSCTL_RCGC2_R & SYSCTL_RCGC2_GPIOF)==0){};
  //GPIO_PORTF_LOCK_R = GPIO_LOCK_KEY; // 2) unlock PortF PF0  
  //GPIO_PORTF_CR_R |= 0x10;           // allow changes to PF4,0       
  GPIO_PORTF_DIR_R &= ~0x10;
  GPIO_PORTF_AFSEL_R &= ~0x10;       //     disable alt funct 
  GPIO_PORTF_DEN_R |= 0x10;          //     enable digital   
  GPIO_PORTF_PCTL_R &= ~0x000F0000; // configure as GPIO
  GPIO_PORTF_AMSEL_R &= ~0x10;       //     disable analog functionality on PF
  GPIO_PORTF_PUR_R |= 0x10;     //     enable weak pull-up on PF0,4
		
  GPIO_PORTF_IS_R &= ~0x10;     // (d) PF0,4 is edge-sensitive
  GPIO_PORTF_IBE_R &= ~0x10;    //     PF0,4 is not both edges
  GPIO_PORTF_IEV_R &= ~0x10;    //     PF4 falling edge event
  GPIO_PORTF_ICR_R = 0x10;      // (e) clear flag4
  GPIO_PORTF_IM_R |= 0x10;      // (f) arm interrupt on PF4
  NVIC_PRI7_R = (NVIC_PRI7_R&~0x00E00000)|0x00800000; // (g) priority 4
  NVIC_EN0_R |= 0x40000000;      // (h) enable interrupt 30 in NVIC
		
}

// Subroutine to wait 10 msec
// Inputs: None
// Outputs: None
// Notes: ...
void DelayWait10ms(uint32_t n){uint32_t volatile time;
  while(n){
//    time = 727240*2/91;  // 10msec for launchpad
    time = 727240/91;  // for simulation
    while(time){
	  	time--;
    }
    n--;
  }
}


